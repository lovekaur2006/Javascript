//DO WHILE LOOP

//EXAMPLE 1:
let i = 0;

do {
    console.log(i);
    i++;
} while (i < 4);


//EXAMPLE 2:

let j = 0;
do {
    console.log("The number is : " + j);
    j++;
} while (j < 5);


//EXAMPLE 3:

const fruits = ["Apple", "Banana", "Grapes"];
let a = 0;
do {
    console.log(fruits[a]);
    a++;
} while (fruits[a]);
