//WHILE LOOP

//EXAMPLE 1
let i = 0;

while (i < 4) {
    console.log(i);
    i++;
}


//EXAMPLE 2

let j = 0;
while (j < 5) {
    console.log("The number is : " + j);
    j++;
}


//EXAMPLE 3

const fruits = ["Apple", "Banana", "Grapes"];
let a = 0;
while (fruits[a]) {
    console.log(fruits[a]);
    a++;
}
